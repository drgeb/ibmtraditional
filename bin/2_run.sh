#!/bin/sh
NAME=app
WSADMIN_SSL_CONSOLE_PORT=9043
WSADMIN_HTTPS_PORT=9443
HTTP_TRANSPORT_PORT=9080
DB2_SERVER_PORT=50000
IMAGE=app

docker run --name ${NAME} -h ${NAME} \
    -p ${WSADMIN_SSL_CONSOLE_PORT}:${WSADMIN_SSL_CONSOLE_PORT} \
    -p ${WSADMIN_HTTPS_PORT}:${WSADMIN_HTTPS_PORT} \
    -p ${HTTP_TRANSPORT_PORT}:${HTTP_TRANSPORT_PORT} \
    -p ${DB2_SERVER_PORT}:${DB2_SERVER_PORT} \
    -d ${IMAGE}